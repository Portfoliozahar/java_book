// Vector — реализация динамического массива объектов, методы которой синхронизированы.

package Collec.List;

import java.util.Vector;

public class VVector {
    public static void main(String[] args) {
        // Создаем объект Vector
        Vector<String> vector = new Vector<>();

        // Добавляем элементы в вектор
        vector.add("Элемент 1");
        vector.add("Элемент 2");
        vector.add("Элемент 3");

        // Выводим содержимое вектора
        System.out.println("Содержимое вектора: " + vector);

        // Получаем размер вектора
        int size = vector.size();
        System.out.println("Размер вектора: " + size);

        // Получаем элемент по индексу
        String element = vector.get(1);
        System.out.println("Элемент по индексу 1: " + element);

        // Изменяем элемент по индексу
        vector.set(1, "Новый элемент");
        System.out.println("Измененное содержимое вектора: " + vector);

        // Удаляем элемент по индексу
        vector.remove(0);
        System.out.println("Содержимое вектора после удаления элемента: " + vector);

        // Проверяем наличие элемента в векторе
        boolean contains = vector.contains("Новый элемент");
        System.out.println("Вектор содержит элемент 'Новый элемент': " + contains);

        // Очищаем вектор
        vector.clear();
        System.out.println("Содержимое вектора после очистки: " + vector);

        // Проверяем, пуст ли вектор
        boolean isEmpty = vector.isEmpty();
        System.out.println("Вектор пустой: " + isEmpty);
    }
}

// Одной из главных преимуществ реализации Vector является то, что все методы
// этого класса синхронизированы.
// Это позволяет использовать Vector в многопоточной среде, где несколько
// потоков могут обращаться к вектору одновременно безопасно.
// Кроме того, Vector автоматически увеличивает свою емкость при добавлении
// новых элементов,
// что обеспечивает динамическое расширение массива. Это позволяет работать с
// коллекцией,
// которая может расти по мере необходимости, без необходимости вручную
// управлять ее размером.